export function companyToSlug(name) {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

export function slugToCompany(slug) {
  const found = COMPANIES.find(c => companyToSlug(c.name) === slug);
  return found ? found.name : slug.replace(/-/g, ' ').replace(/\b\w/g, ch => ch.toUpperCase());
}

